      (5, 3, 5, 1, '2022-04-03'),
      (6, 2, 4, 1, '2022-04-06'),
      (2, 1, 4, 2, '2022-05-11');
---

--- DML(SELECT)
SELECT USER_ID, PRODUCT_ID
FROM ONLINE_SALE
GROUP BY USER_ID, PRODUCT_ID
HAVING COUNT(ONLINE_SALE_ID) > 1
ORDER BY USER_ID ASC, PRODUCT_ID DESC;
